

// Ejercicio 1: Sumar los Números del 1 al 10 usando un bucle for

let suma = 0; // Esta variable es para almacenar la suma

for (let i = 1; i <= 10; i++) { // Utilizo el operador <= (menor o igual que) para que 10 sea el límite 

   suma += i;  // Acá utilizo el operador += para sumar el valor de i a la variable suma 
}

console.log('Suma de los numeros del 1 al 10:', suma); // Imprimir en consola 